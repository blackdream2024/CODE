interface ChartState {
  // 命盘数据
  charts: ChartData[];
  currentChart: ChartData | null;

  // 推演结果
  simulationResult: SimulationResult | null;
  isSimulating: boolean;

  // 场景选择
  selectedScenario: ScenarioType;

  // Actions
  addChart: (chart: ChartData) => void;
  removeChart: (id: string) => void;
  setCurrentChart: (chart: ChartData | null) => void;
  updateChart: (id: string, updates: Partial<ChartData>) => void;

  setSimulationResult: (result: SimulationResult | null) => void;
  setIsSimulating: (loading: boolean) => void;

  setSelectedScenario: (scenario: ScenarioType) => void;

  // 计算属性
  getChartById: (id: string) => ChartData | undefined;
}

export const useChartStore = create<ChartState>((set, get) => ({
  charts: [sampleChartData1, sampleChartData2],
  currentChart: sampleChartData1,
  simulationResult: null,
  isSimulating: false,
  selectedScenario: 'career',

  addChart: (chart) =>
    set((state) => ({
      charts: [...state.charts, chart],
    })),

  removeChart: (id) =>
    set((state) => ({
      charts: state.charts.filter((c) => c.id !== id),
      currentChart: state.currentChart?.id === id ? null : state.currentChart,
    })),

  setCurrentChart: (chart) =>
    set({ currentChart: chart }),

  updateChart: (id, updates) =>
    set((state) => ({
      charts: state.charts.map((c) =>
        c.id === id ? { ...c, ...updates } : c
      ),
      currentChart:
        state.currentChart?.id === id
          ? { ...state.currentChart, ...updates }
          : state.currentChart,
    })),

  setSimulationResult: (result) =>
    set({ simulationResult: result }),

  setIsSimulating: (loading) =>
    set({ isSimulating: loading }),

  setSelectedScenario: (scenario) =>
    set({ selectedScenario: scenario }),

  getChartById: (id) => {
    return get().charts.find((c) => c.id === id);
  },
}));
